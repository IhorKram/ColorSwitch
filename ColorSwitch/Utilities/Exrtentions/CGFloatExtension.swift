import UIKit


extension CGFloat {
    static func padding() -> CGFloat { return 10 }
    static func paddingTextField() -> CGFloat { return 30 }
    static func heightButton() -> CGFloat { return 60 }
    static func heightTitleLabel() -> CGFloat { return 25 }
    static func heightTextField() -> CGFloat { return 40 }
    static func cvMinimumLineSpacing() -> CGFloat { return 40 }
    static func justLineWidth() -> CGFloat { return 30 }
}
